export function getMicroLocationsByCity(citySlug: string): MicroLocation[] {
  return microLocations.filter((loc) => loc.citySlug === citySlug)
}

export function getMicroLocation(citySlug: string, areaSlug: string): MicroLocation | undefined {
  return microLocations.find((loc) => loc.citySlug === citySlug && loc.areaSlug === areaSlug)
}

export function slugifyAreaName(areaName: string): string {
  return areaName
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
}
